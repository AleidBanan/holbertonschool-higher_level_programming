#!/usr/bin/python3
"""Lists states matching the given name (safe from SQL injection)"""

import sys
import MySQLdb


if __name__ == "__main__":
    db = MySQLdb.connect(
        host="localhost",
        port=3306,
        user=sys.argv[1],
        passwd=sys.argv[2],
        db=sys.argv[3]
    )

    c = db.cursor()
    c.execute(
        "SELECT * FROM states WHERE name = %s ORDER BY id ASC;",
        (sys.argv[4],)
    )

    rows = c.fetchall()
    for row in rows:
        print(row)

    c.close()
    db.close()
